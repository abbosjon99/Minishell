#include "ft_printf.h"

int	ft_putst(char *str)
{
	int	i;

	if (!str)
	{
		ft_putst("(null)");
		return (6);
	}
	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
