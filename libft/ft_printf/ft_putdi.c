#include "ft_printf.h"

int	ft_putdi(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
		len = ft_putst("-2147483648");
	else
	{
		if (n < 0)
		{
			n = -n;
			len += ft_putch('-');
		}
		if (n > 9)
		{
			len += ft_putdi(n / 10);
		}
		len += ft_putch(n % 10 + '0');
	}
	return (len);
}
