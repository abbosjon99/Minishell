#include "ft_printf.h"

int	ft_putu(unsigned int n)
{
	int	len;

	len = 0;
	if (n > 9)
	{
		len += ft_putu(n / 10);
	}
	len += ft_putch(n % 10 + '0');
	return (len);
}
